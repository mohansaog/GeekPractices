package StringBasic;

public class StringDemo {

	public static void main(String[] args) {
		String s = "hello";
		System.out.println(s.length()); // to get length
		System.out.println(s.charAt(2)); // to print perticular index
		System.out.println(s.substring(1,3));// print except last indx from initial
		System.out.println(s.substring(1));
	}

}
